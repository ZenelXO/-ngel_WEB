const title = document.getElementById('title');
const originalTitle = title.innerText;
function moveTitle() {
    // Obtener el texto del título y mover el primer carácter al final
    const newText = title.innerText.substring(1) + title.innerText.substring(0, 1);
    title.innerText = newText;
}
setInterval(moveTitle, 300);

